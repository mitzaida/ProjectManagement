package com.arrayList;

public class Areas {
    private int codArea;
    private String desArea;

    public Areas(int codArea, String desArea) {
        this.codArea = codArea;
        this.desArea = desArea;
    }

    public int getCodArea() {
        return codArea;
    }

    public String getDesArea() {
        return desArea;
    }

}
